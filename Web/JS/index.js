function myBlog_mouseover() {
    var myBlogId = document.getElementById("myBlogId");
    myBlogId.style.borderColor = "white";
    myBlogId.style.backgroundColor = "white";
    myBlogId.style.fontColor = "black";
    
}
function myBlog_mouseout() {
    var myBlogId = document.getElementById("myBlogId");
    myBlogId.style.border = "2px";
}